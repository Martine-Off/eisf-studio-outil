const { validationResult } = require('express-validator');

const validate = (rules) => [
    ...rules,
    (req, res, next) => {
        const errors = validationResult(req);
        if (!errors.isEmpty()) {
            return res.status(400).json({ error: errors.array()[0].msg });
        }
        next();
    },
];

module.exports = { validate };
